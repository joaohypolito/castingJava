// Declare uma variável do tipo double e uma variável do tipo int. Faça o casting da variável double para int e imprima o resultado.
public class Main {

    public static void main(String[] args) {

        double numberDouble = 13.5;
        int numberInt = (int)numberDouble; // O casting em Java é a conversão de um tipo de dado para outro.

        System.out.println("Número double: " + numberDouble);
        System.out.println("Número inteiro: " + numberInt);

    }
    
}